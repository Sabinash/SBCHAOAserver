public class Oppinion {

    String fltNbr;
    String name;
    String oppinion;
    String commets;

    public String getFltNbr() {
        return fltNbr;
    }

    public void setFltNbr(String fltNbr) {
        this.fltNbr = fltNbr;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getOppinion() {
        return oppinion;
    }

    public void setOppinion(String oppinion) {
        this.oppinion = oppinion;
    }

    public String getCommets() {
        return commets;
    }

    public void setCommets(String commets) {
        this.commets = commets;
    }

}
